            <h4 class="color-motive">Avaliação</h4>
            Solicitamos inicialmente preenchimento de questionário prévio. Na avaliação será determinado junto aos responsáveis o plano de tratamento de acordo com o quadro clínico apresentado, idade, habilidade motora e alterações osteomusculares associadas. Para isso solicitamos que os responsáveis apresentem o máximo de informações possíveis conforme listados abaixo:</p>
            <ul>
            <li>Relatório de alta da maternidade e de outras internações, caso que tenha ocorrido,</li>
            <li>Relatório médico neurológico ou ortopédico,</li>
            <li>Ressonância Magnética,</li>
            <li>Tomografia Computadorizada,</li>
            <li>RX Quadril realizado nos últimos seis meses,</li>
            <li>RX Coluna realizado nos últimos seis meses,</li>
            <li>Exames da deglutição (nasofibroscopia, deglutograma, videoendoscopia),</li>
            <li>Exames da audição (bera, audiometria, emissões otoacústicas),</li>
            <li>Exames gastroenterológicos (endoscopia, cintilografia, etc),</li>
            <li>Questionário prévio preenchido,</li>
            <li>Relatório de fisioterapia, terapia ocupacional ou fonoaudiologia do profissional que acompanha o paciente, especificando o trabalho desenvolvido e</li>
            <li>Relatório de alta, caso já tenha realizado fisioterapia intensiva.</li>
            </ul>
