
            <h4 class="color-motive">A Therapies4Kids realiza reabilitação de forma convencional ou intensiva.</h4>
            <p>Contamos com equipe interdisciplinar incluindo fisioterapia, terapia ocupacional, fonoaudiologia, psicopedagogia, psicologia e musicoterapia.</p>
            <p>Os atendimentos são realizados em sessões com duração de 1 a 4 horas na frequência de até 3x por semana.</p>
            <p>As órteses corporais Pediasuit ou Theratogs, são utilizadas em todos os atendimentos de fisioterapia e terapia ocupacional com duração mínima de 2 horas.</p>
            <p>Os programas intensivos são realizados com duração mínima de 2 horas diárias pelo período de até 4 semanas.</p>
            <p>No tratamento intensivo, é possível montar um programa interdisciplinar, de acordo com a demanda do paciente.</p>
            <p>Elaboramos programa de tratamento de acordo com o quadro clínico, idade, habilidade motora e alterações ósseomusculares apresentadas:</p>

            <h4 class="color-motive"> PEDIASUIT</h4>
            <p>O Pediasuit é uma vestimenta ortopédica macia e dinâmica, consiste em chapéu, colete, calção, joelheiras e calçados adaptados que são interligados por tracionadoes elásticos e emborrachados.</p>
            <p>O conceito básico do Pediasuit é a criação de uma unidade de suporte para o corpo, estabelecendo alinhamento biomecânico e descarga de peso que são fundamentais na normalização do tônus muscular, da função sensorial e vestibular.</p>
            <p>Os tracionadores são ajustáveis, o que significa que se pode aplicar axialmente no corpo uma descarga de 15 a 40 kg.</p>

            <h4 class="color-motive">THERATOGS</h4>
            <p>O Theratogs é uma órtese corporal dinâmica, composta por duas peças de vestuário e conjunto de tiras. O material usado no PTA é o tecido especial goldtone, específico para o treinamento neuromotor, auxilia no trabalho muscular, permitindo o alinhamento da postura, melhora da capacidade respiratória e o aumento da consciência sensorial.</p>

            <h4 class="color-motive">SPIDER E MONKEY CAGE</h4>
            <p>A Spider Cage é uma estrutura em que o controle postural e funcional é facilitado pela utilização de bungees elásticos, onde o paciente pode ter maior ou menor sustentação de acordo com a atividade e funções trabalhadas; e a Monkey Cage é uma estrutura em que é realizado o fortalecimento muscular específico através de sistema de polias.</p>
            <p>As condutas terapêuticas baseiam-se nos conceitos de cinesioterapia e em abordagens e métodos terapêuticos, tais como Cuevas Medek, Padovan, Integração Sensorial, Kabat, Balance e Bobath.</p>

