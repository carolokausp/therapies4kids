                     <p>Autism Spectrum Disorder (ASD) is a severe disruption of normal developmental processes that occurs early in life. It leads to impaired language, play, cognitive, social and adaptive functioning, causing children to fall farther and farther behind their peers as they grow older. The causes of ASD are unknown, but evidence points to a genetic basis, with children with ASD having neurological differences in certain areas of the brain.</p>
                     <p>Although children with an ASD exhibit social communication deficits along with restricted/repetitive behaviours, there are tremendous differences among children in how these areas are displayed.  Children with autism do not learn in the same way that children normally learn. However, what is most important to note is that – with the right type of intervention – children with ASD have an amazing ability to learn and grow.</p>
                     <h4 class="color-motive">A.B.A. - What is Applied Behaviour Analysis?</h4>

                     <p>Applied Behaviour Analysis (ABA) is a field of psychology that focuses on applying learning theories to different people and situations. One specific application of these techniques is early, intensive, behavioral intervention (EIBI) for children with autism.
                     The techniques of ABA are grounded in science.  Some of the hallmarks of ABA include:</p>

                     <ul>
                         <li>Breaking complex tasks into smaller tasks that can be taught more easily</li>
                         <li>Providing repeated opportunities to learn and master new skills</li>
                         <li>Using reinforcement procedures to assist in the acquisition of new skills</li>
                         <li>Understanding the functions of behaviors (e.g. communication, attention seeking, avoidance, etc.) and developing programs to meet the child’s needs</li>
                         <li>Making objective, data driven decisions to guide our evaluations of treatment progress</li>
                         <li>Utilizing effective teaching techniques, such as discrete trial teaching (DTT)</li>
                     </ul>

