                    <h4 class="color-motive">How Occupational Therapy Will Help</h4>
                    <p>The therapist carefully analyses the sensory, physical, cognitive and behavioral aspects causing the child to have difficulties in his/ her life occupations.</br>
                    Intervention is then targeted at the weak areas to improve the underlying skills. Intervention looks at a combination of:</p>
                    <ul>
                         <li>Education of child and adult caregivers as to the reasons for the difficulties.</li>
                         <li>Environmental modification where appropriate to help ensure that the child has the “just right” level of challenge to enable success.</li>
